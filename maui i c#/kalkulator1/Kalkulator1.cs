namespace kalkulator1
{
    public class Kalkulator1
    {
        public int Dodawanie(int a, int b)
        {
            return a + b;
        }
        public int Odejmowanie(int a, int b)
        {
            return a - b;
        }
        public int Mnozenie(int a, int b)
        {
            return a * b;
        }
        public double Dzielenie(int a, int b)
        {
            return (double)a / b;
        }
    }
}
